package Part3_JulyLoop;
/*
Write a  for loop that prints the odd integers 11 through 121 inclusive, separated by spaces.
 */
public class PrintOddNumbersLoop {
    public static void main(String[] args) {
        int i;
        for(i=11; i<=121; i+=2){
            System.out.print(i+" ");
        }


    }
}
