package Part5_July_Methods;
/*
isEven method gets a number(int) if its even (2,4,8...) returns true.
if its odd return false.

for example:

isEven(1) --> false

isEven(8) --> true


 */
public class MethodsWithReturn3IsEven {


    public boolean isEven(int n)
    {
        return n%2 ==0;
    }


}
